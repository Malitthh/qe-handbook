package simple.interview.questions;

public class PrintOddNums {
	public static void main(String[] args) {
		//Print Odd Numbers
		for (int a = 1; a <= 100; a += 2) {
			System.out.println(a);
		}
		//Print 3 to 10
		for (int b = 3; b <= 10; b++) {
			System.out.println(b);
		}
	}

}
